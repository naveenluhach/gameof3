package com.gameof3.gameof3artifact.User;

/**
 * Enumeration representing the status of a user in the application.
 * Users can have statuses "ONLINE" or "OFFLINE".
 */
public enum Status {
    /**
     * Represents that the user is online.
     */
    ONLINE,

    /**
     * Represents that the user is offline.
     */
    OFFLINE
}